#define _CRT_SECURE_NO_WARNINGS 

#include <stdio.h>
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Function: main
//Purpose: Convert signed or unsigned decimal into binary bit values
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
	int n, c, k;

	printf("Enter an integer in decimal number system: \n");
	scanf("%d", &n);

	printf("%d in binary number system is:\n", n);

	for (c = 15; c >= 0; c--)	//for loop is for 16bits
	{
		k = n >> c;				//n shift right 15 times to the LSB, then 14 times.... until 0.

		if (k & 1)				//if k is 1, print 1
			printf("1");
		else
			printf("0");
	}

	printf("\n");

	getch();

	return 0;
}